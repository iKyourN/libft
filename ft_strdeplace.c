#include "libft.h"

void	ft_strdeplace(char **str, int deplace, int i)
{
	while ((*str)[i] != '\0')
	{
		(*str)[i - deplace] = (*str)[i];
		i++;
	}
	(*str)[i - deplace] = '\0';
}
